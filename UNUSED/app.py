@app.post("/api/compiler/merge", summary="Agent 4: Isolated DOM Tree Merger Engine")
async def compiler_dom_merge(request: CommitMergeRequest):
    """Surgically appends new code slices inside the live workspace DOM architecture tree safely using BeautifulSoup."""
    try:
        if not workspace.current_html:
            soup = BeautifulSoup(workspace.get_scaffold(), "html_parser")
        else:
            soup = BeautifulSoup(workspace.current_html, "html_parser")

        container = soup.find(id="landing-builder-sections")
        if not container:
            raise HTTPException(status_code=500, detail="The virtual workspace tree structure is missing its root grid anchor.")

        section_slice = BeautifulSoup(request.section_html, "html_parser")
        container.append(section_slice)

        # Append script to re-trigger Lucide icon evaluations on runtime additions
        if soup.body and not soup.find(id="lucide-reinit-script"):
            reinit_script = soup.new_tag("script", id="lucide-reinit-script")
            reinit_script.string = "lucide.createIcons();"
            soup.body.append(reinit_script)

        workspace.current_html = str(soup)
        return {"status": "success", "compiled_html": workspace.current_html}
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Surgical tree compilation fault: {str(e)}")


