@app.post("/api/agents/vision", summary="Agent 1: Multimodal Vision UI/UX Analyzer")
async def agent_vision_analysis(request: AnalyzeVisionRequest):
    """Deconstructs screenshot slices into granular styling tokens, extracted layout and copy schemas."""
    if "," in request.image_base64:
        request.image_base64 = request.image_base64.split(",")[1]

    system_instruction = (
        "Deconstruct the landing page section screenshot into structural tokens. Extract the type of layout, "
        "exact hexadecimal color values, font typography profiles, all contained copy strings, and asset requirements. "
        "For image requirements, write complex photographic prompts in English optimized for an image generation agent."
    )
    
    user_prompt = f"Analyze this user interface block. Additional user branding rules: {request.brand_context}"

    response_schema = {
        "type": "OBJECT",
        "properties": {
            "section_type": { "type": "STRING", "description": "hero, cta, features, pricing, faq, or testimonials" },
            "color_palette": {
                "type": "OBJECT",
                "properties": {
                    "bg": { "type": "STRING" },
                    "text_primary": { "type": "STRING" },
                    "accents": { "type": "ARRAY", "items": { "type": "STRING" } }
                },
                "required": ["bg", "text_primary", "accents"]
            },
            "typography": {
                "type": "OBJECT",
                "properties": {
                    "style": { "type": "STRING", "description": "serif, sans, display" },
                    "font_family": { "type": "STRING", "description": "Google Font name" }
                },
                "required": ["style", "font_family"]
            },
            "extracted_copy": {
                "type": "OBJECT",
                "properties": {
                    "title": { "type": "STRING" },
                    "subtitle": { "type": "STRING" },
                    "paragraphs": { "type": "ARRAY", "items": { "type": "STRING" } },
                    "cta_label": { "type": "STRING" }
                },
                "required": ["title", "subtitle", "paragraphs", "cta_label"]
            },
            "image_requirements": {
                "type": "ARRAY",
                "items": {
                    "type": "OBJECT",
                    "properties": {
                        "id": { "type": "STRING" },
                        "ai_generation_prompt": { "type": "STRING" },
                        "fallback_keywords": { "type": "ARRAY", "items": { "type": "STRING" } },
                        "aspect_ratio": { "type": "STRING" }
                    },
                    "required": ["id", "ai_generation_prompt", "fallback_keywords", "aspect_ratio"]
                }
            }
        },
        "required": ["section_type", "color_palette", "typography", "extracted_copy", "image_requirements"]
    }

    payload = {
        "contents": [{
            "parts": [
                {"text": user_prompt},
                {"inlineData": {"mimeType": "image/png", "data": request.image_base64}}
            ]
        }],
        "systemInstruction": {"parts": [{"text": system_instruction}]},
        "generationConfig": {
            "responseMimeType": "application/json",
            "responseSchema": response_schema
        }
    }

    raw_response = await request_gemini_engine("gemini-2.5-flash-preview-09-2025:generateContent", payload)
    text_out = raw_response["candidates"][0]["content"]["parts"][0]["text"]
    return JSONResponse(content=json.loads(text_out))


@app.post("/api/agents/assets", summary="Agent 2: Autonomous Asset Generator (Imagen 4.0)")
async def agent_asset_resolution(request: ResolveAssetRequest):
    """Generates ultra-high resolution photography assets with Imagen 4.0 or triggers stock fallback mechanics."""
    payload = {
        "instances": [{"prompt": request.prompt}],
        "parameters": {
            "sampleCount": 1,
            "aspectRatio": request.aspect_ratio,
            "outputMimeType": "image/png"
        }
    }

    try:
        url = f"imagen-4.0-generate-001:predict?key={GEMINI_API_KEY}"
        async with httpx.AsyncClient() as client:
            response = await client.post(f"{GEMINI_API_URL}/{url}", json=payload, timeout=60.0)
            if response.status_code == 200:
                img_data = response.json()["predictions"][0]["bytesBase64Encoded"]
                return {"secure_url": f"data:image/png;base64,{img_data}"}
            
            logger.warning("Imagen 4.0 bypassed or limited. Executing high-quality unsplash asset fallback match.")
            return {"secure_url": "https://images.unsplash.com/photo-1570172619644-dfd03ed5d881?w=800&auto=format&fit=crop&q=80"}
    except Exception as e:
        logger.error(f"Fallback asset route triggered due to: {str(e)}")
        return {"secure_url": "https://images.unsplash.com/photo-1512290923902-8a9f81dc236c?w=800&auto=format&fit=crop&q=80"}


