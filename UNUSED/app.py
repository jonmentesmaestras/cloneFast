# --- CORE AGENTS SERVICE PIPELINE ---

async def request_gemini_engine(endpoint: str, payload: dict) -> dict:
    """Helper connection pipeline to interface securely with the Gemini Engine API."""
    url = f"{GEMINI_API_URL}/{endpoint}?key={GEMINI_API_KEY}"
    async with httpx.AsyncClient() as client:
        try:
            response = await client.post(url, json=payload, timeout=60.0)
            if response.status_code == 200:
                return response.json()
            raise HTTPException(
                status_code=response.status_code, 
                detail=f"Gemini Engine execution error: {response.text}"
            )
        except Exception as e:
            logger.error(f"Network abstraction fault connecting to Gemini API: {str(e)}")
            raise HTTPException(status_code=500, detail=f"AI Engine link failure: {str(e)}")


